const LANGUAGE_STORAGE_KEY = 'biomaussan-language';
const THEME_STORAGE_KEY = 'biomaussan-theme';

let currentLang = localStorage.getItem(LANGUAGE_STORAGE_KEY) || 'en';
const body = document.body;
const themeButtons = [...document.querySelectorAll('.theme-toggle')];

function getNestedValue(object, path) {
  return path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : ''), object);
}

function setTheme(mode) {
  const isDark = mode === 'dark';
  const langPack = translations[currentLang];
  body.setAttribute('data-theme', isDark ? 'dark' : 'light');

  themeButtons.forEach((button) => {
    const text = button.querySelector('.theme-toggle-text');
    const icon = button.querySelector('.theme-toggle-icon');
    if (text) text.textContent = isDark ? langPack.ui.themeLight : langPack.ui.themeDark;
    if (icon) icon.textContent = isDark ? '☀️' : '🌙';
    button.setAttribute('aria-label', isDark ? langPack.ui.switchToLight : langPack.ui.switchToDark);
    button.setAttribute('aria-pressed', String(isDark));
  });
}

function applyLanguage(lang) {
  currentLang = translations[lang] ? lang : 'en';
  const langPack = translations[currentLang];

  document.documentElement.lang = currentLang;
  localStorage.setItem(LANGUAGE_STORAGE_KEY, currentLang);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const value = getNestedValue(langPack, el.dataset.i18n);
    if (typeof value === 'string') el.textContent = value;
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const value = getNestedValue(langPack, el.dataset.i18nHtml);
    if (typeof value === 'string') el.innerHTML = value;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const value = getNestedValue(langPack, el.dataset.i18nPlaceholder);
    if (typeof value === 'string') el.setAttribute('placeholder', value);
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
    const value = getNestedValue(langPack, el.dataset.i18nAriaLabel);
    if (typeof value === 'string') el.setAttribute('aria-label', value);
  });

  document.querySelectorAll('.lang-btn').forEach((button) => {
    const isActive = button.dataset.lang === currentLang;
    button.classList.toggle('active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });

  const currentTheme = body.getAttribute('data-theme') === 'dark' ? 'dark' : 'light';
  setTheme(currentTheme);
}

const savedTheme = localStorage.getItem(THEME_STORAGE_KEY);
setTheme(savedTheme === 'dark' ? 'dark' : 'light');

applyLanguage(currentLang);

themeButtons.forEach((button) => {
  button.addEventListener('click', () => {
    const nextTheme = body.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
    setTheme(nextTheme);
    localStorage.setItem(THEME_STORAGE_KEY, nextTheme);
  });
});

document.querySelectorAll('.lang-btn').forEach((button) => {
  button.addEventListener('click', () => applyLanguage(button.dataset.lang));
});

const slides = [...document.querySelectorAll('.slide')];
const dotsWrap = document.querySelector('.slider-dots');
let current = 0;

slides.forEach((_, i) => {
  const btn = document.createElement('button');
  if (i === 0) btn.classList.add('active');
  btn.addEventListener('click', () => showSlide(i));
  dotsWrap.appendChild(btn);
});

const dots = [...dotsWrap.children];

function showSlide(index) {
  slides[current].classList.remove('active');
  dots[current].classList.remove('active');
  current = index;
  slides[current].classList.add('active');
  dots[current].classList.add('active');
}

setInterval(() => showSlide((current + 1) % slides.length), 4500);

const io = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) entry.target.classList.add('in');
  });
}, { threshold: 0.14 });

document.querySelectorAll('.reveal').forEach((el) => io.observe(el));

const toggle = document.querySelector('.menu-toggle');
const mobileMenu = document.querySelector('.mobile-menu');
if (toggle) {
  toggle.addEventListener('click', () => mobileMenu.classList.toggle('open'));
}

document.querySelectorAll('.mobile-menu a').forEach((link) => link.addEventListener('click', () => mobileMenu.classList.remove('open')));

